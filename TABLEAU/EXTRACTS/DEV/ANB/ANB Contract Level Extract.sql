) AS "Submit Date Is Holiday"

,T1.AGREEMENTID

, T12.PROD_CD

, TRIM(LEADING '0' FROM HOLDINGKEY) as HOLDINGKEY

, T1.ORDERENTRYID

,T4.AUTOAPPROVEDINDICATOR

,Case

