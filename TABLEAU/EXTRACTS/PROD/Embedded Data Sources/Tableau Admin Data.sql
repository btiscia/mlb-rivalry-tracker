,HE.ID EVENT_ID
,HET.NAME EVENT_NAME
,HET.ACTION_TYPE
,HU.NAME MMID
,HU.EMAIL USER_EMAIL
,SU.FRIENDLY_NAME USERNAME
,HV.NAME VIEW_NAME
,CASE
	WHEN HW.NAME IS NULL THEN HD.NAME 
	ELSE HW.NAME
END DATA_SOURCE_NAME
,HW.NAME WORKBOOK_NAME
,HP.NAME PROJECT_NAME
,BJ1.STARTED_AT EVENT_START_DATETIME
,BJ1.COMPLETED_AT EVENT_END_DATETIME
,BJ1.FINISH_CODE FAIL_FLAG 
,HS.NAME SITE_NAME
