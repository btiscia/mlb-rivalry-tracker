SELECT 
'ACCESS' USAGE_TYPE
,HE.ID EVENT_ID
,HET.NAME EVENT_NAME
,HET.ACTION_TYPE
,HU.NAME MMID
,HU.EMAIL USER_EMAIL
,SU.FRIENDLY_NAME USERNAME
,HV.NAME VIEW_NAME
,HD.NAME DATA_SOURCE_NAME
,HW.NAME WORKBOOK_NAME
,HP.NAME PROJECT_NAME
,CAST(HE.CREATED_AT AS DATE) EVENT_DATE
,HE.CREATED_AT EVENT_START_DATETIME
,NULL EVENT_END_DATETIME
,0 FAIL_FLAG
,HS.NAME SITE_NAME
