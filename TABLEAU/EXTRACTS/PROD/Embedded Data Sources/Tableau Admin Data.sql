'FAILURE' USAGE_TYPE
,BJ.ID EVENT_ID
,'Data Refresh Failure' EVENT_NAME
,BJ.JOB_NAME
,NULL
,NULL
,NULL
,BJ.TITLE VIEW_NAME
,BJ.TITLE DATA_SOURCE_NAME
,BJ.TITLE  WORKBOOK_NAME
,NULL PROJECT_NAME
,CAST(BJ.STARTED_AT AS DATE) EVENT_DATE
,BJ.STARTED_AT EVENT_START_DATETIME
,BJ.COMPLETED_AT EVENT_END_DATETIME
,BJ.FINISH_CODE FAIL_FLAG 
