ON HS.ID = HE.HIST_TARGET_SITE_ID
LEFT JOIN WORKGROUP.PUBLIC.BACKGROUND_JOBS BJ1
ON SUBSTRING(BJ1.NOTES, POSITION('{' IN BJ1.NOTES) + 1, (POSITION('}' IN BJ1.NOTES) - POSITION('{' IN BJ1.NOTES))) = 
	SUBSTRING(DETAILS, POSITION('{' IN HE.DETAILS) + 1, (POSITION('}' IN HE.DETAILS) - POSITION('{' IN HE.DETAILS)))
WHERE HS.SITE_ID IN (4,18)
AND HET.TYPE_ID IN (130,131)
AND BJ1.JOB_TYPE LIKE '%refresh_extracts%'
AND SUBSTRING(BJ1.NOTES, POSITION('{' IN BJ1.NOTES) + 1, (POSITION('}' IN BJ1.NOTES) - POSITION('{' IN BJ1.NOTES))) <> '') UPDATE_SUB

UNION

