USAGE_TYPE
,EVENT_ID
,EVENT_NAME
,ACTION_TYPE
,MMID
,USER_EMAIL
,USERNAME
,VIEW_NAME
,DATA_SOURCE_NAME
,COALESCE(WORKBOOK_NAME, DATA_SOURCE_NAME)
,PROJECT_NAME
