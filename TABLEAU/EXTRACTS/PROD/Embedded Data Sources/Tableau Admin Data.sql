ON HV.ID = HE.HIST_VIEW_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_WORKBOOKS HW
ON HW.ID = HE.HIST_WORKBOOK_ID
LEFT JOIN WORKGROUP.PUBLIC.WORKBOOKS W
ON W.ID = HW.WORKBOOK_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_PROJECTS HP
ON HP.ID = HE.HIST_PROJECT_ID
LEFT JOIN WORKGROUP.PUBLIC.PROJECTS P
ON P.ID = HP.PROJECT_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_USERS HU
ON HE.HIST_ACTOR_USER_ID = HU.ID
LEFT JOIN WORKGROUP.PUBLIC.USERS U
ON U.ID = HU.USER_ID
LEFT JOIN WORKGROUP.PUBLIC.SYSTEM_USERS SU
ON SU.ID = U.SYSTEM_USER_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_SITES HS
ON HS.ID = HE.HIST_TARGET_SITE_ID
WHERE HS.SITE_ID IN (4,18)
AND HET.TYPE_ID IN (84,112)

UNION

