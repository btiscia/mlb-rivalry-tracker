SELECT 
USAGE_TYPE
,ROW_NUMBER() OVER () EVENT_ID
,EVENT_NAME
,ACTION_TYPE
,MMID
,USER_EMAIL
,USERNAME
,VIEW_NAME
,DATA_SOURCE_NAME
,WORKBOOK_NAME
,PROJECT_NAME
,CAST(EVENT_START_DATETIME AS DATE) EVENT_DATE
,EVENT_START_DATETIME
,EVENT_END_DATETIME
,FAIL_FLAG
,SITE_NAME
FROM
(SELECT DISTINCT
'LOAD' USAGE_TYPE
,CASE 
	WHEN LEFT(HTTP_REQUEST_URI,7) = '/views/' THEN 'Load Report'
	WHEN LEFT(HTTP_REQUEST_URI,21) = '/vizql/showadminview/' THEN 'Load Report'
	WHEN HTTP_REQUEST_URI LIKE '%bootstrapSession%' THEN 'Load Report'
	WHEN HTTP_REQUEST_URI LIKE '%set-parameter-value%' THEN 'Interaction'
	WHEN HTTP_REQUEST_URI LIKE '%select-region%' THEN 'Interaction'
	WHEN HTTP_REQUEST_URI LIKE '%categorical-filter%' THEN 'Interaction'
	WHEN HTTP_REQUEST_URI LIKE '%relative-date-filter%' THEN 'Interaction'
	WHEN HTTP_REQUEST_URI LIKE '%select' THEN 'Interaction'
	ELSE 'Other'
END EVENT_NAME
,NULL ACTION_TYPE
,HU.NAME MMID
,HU.EMAIL USER_EMAIL
,SU.FRIENDLY_NAME USERNAME
,CASE
	WHEN CURRENTSHEET = '' THEN NULL
	ELSE SUBSTRING(CURRENTSHEET, POSITION('/' IN CURRENTSHEET) + 1, LENGTH(CURRENTSHEET)) 
END VIEW_NAME 
,NULL DATA_SOURCE_NAME
,CASE
	WHEN CURRENTSHEET = '' THEN NULL
	ELSE SUBSTRING(CURRENTSHEET, 1, POSITION('/' IN CURRENTSHEET) - 1) 
END WORKBOOK_NAME
,P.NAME PROJECT_NAME
,HR.CREATED_AT EVENT_START_DATETIME
,HR.COMPLETED_AT EVENT_END_DATETIME
,0 FAIL_FLAG
,HTTP_REQUEST_URI
,HS.NAME SITE_NAME
FROM WORKGROUP.PUBLIC.HTTP_REQUESTS HR
LEFT JOIN WORKGROUP.PUBLIC.USERS U
ON U.ID = HR.USER_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_USERS HU
ON U.ID = HU.USER_ID
LEFT JOIN WORKGROUP.PUBLIC.SYSTEM_USERS SU
ON SU.ID = U.SYSTEM_USER_ID
LEFT JOIN WORKGROUP.PUBLIC.WORKBOOKS W
ON W.REPOSITORY_URL = CASE
	WHEN CURRENTSHEET = '' THEN NULL
	ELSE SUBSTRING(CURRENTSHEET, 1, POSITION('/' IN CURRENTSHEET) - 1) END
LEFT JOIN WORKGROUP.PUBLIC.PROJECTS P
ON P.ID = W.PROJECT_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_SITES HS
ON HR.SITE_ID = HS.SITE_ID
WHERE HR.SITE_ID IN (4,18)
AND HTTP_REQUEST_URI NOT LIKE '%tooltip%'
AND HTTP_REQUEST_URI NOT LIKE '%/tilecache/%'
AND HTTP_REQUEST_URI NOT LIKE '%/set-active-zone'
AND HTTP_REQUEST_URI NOT LIKE '%/tile/%'
AND HTTP_REQUEST_URI NOT LIKE '%browserBackButtonUndo=true%') SPEED_SUB