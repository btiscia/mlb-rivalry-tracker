ON W.ID = HW.WORKBOOK_ID
LEFT JOIN WORKGROUP.PUBLIC.HIST_PROJECTS HP
ON HP.ID = HE.HIST_PROJECT_ID
LEFT JOIN WORKGROUP.PUBLIC.PROJECTS P
ON P.ID = HP.PROJECT_ID
